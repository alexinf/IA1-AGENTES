    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        presupuesto = Integer.parseInt(jTextField1.getText());
        planos = new LinkedList();
        planos.add("../resources/planos/planos_casa/plano_casa1.png");
        planos.add("../resources/planos/planos_casa/plano_casa2.png");
        planos.add("../resources/planos/planos_casa/plano_casa3.png");
        planos.add("../resources/planos/planos_casa/plano_casa4.png");
        planoActual = 0;
        jLabel3.setIcon(new ImageIcon(getClass().getResource(planos.get(planoActual))));
        presupuesto -= 500;
        jTextField1.setText(presupuesto + "");
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        presupuesto = Integer.parseInt(jTextField1.getText());
        presupuesto -= 500;
        jTextField1.setText(presupuesto + "");
        if(planoActual != 3) {
            planoActual++;
            jLabel3.setIcon(new ImageIcon(getClass().getResource(planos.get(planoActual))));
        } else {
            planoActual = 0;
            jLabel3.setIcon(new ImageIcon(getClass().getResource(planos.get(planoActual))));
        }
    }//GEN-LAST:event_jButton3ActionPerformed

